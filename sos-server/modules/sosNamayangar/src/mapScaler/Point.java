package mapScaler;

public class Point {
	private double x;
	private double y;
	private int id;

	@Override
	public String toString() {
		return "Point(" + id + ") [ X= " + x + " , Y= " + y + " ]";
	}

	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
}
